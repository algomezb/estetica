    private void jButtonNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNuevoActionPerformed
        //se agrega un nuevo item a la tabla
        
        if(!"".equals(jTextCodigo)){
            Producto prod=controlProd.consultar(Integer.parseInt(jTextCodigo.getText()));
            if(prod.getNombre()!=null){
                controlTab.insertar(jTable1, prod);
                arrayProd.add(prod);
            }
            else{
                jLaMensaje.setText("El producto no existe");
                jButtonRegProd.setVisible(true);
            }
            total();
        }
        
    }//GEN-LAST:event_jButtonNuevoActionPerformed

    private void jButtonQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonQuitarActionPerformed
        // elimina el item seleccionado de la tabla
        
        int select=jTable1.getSelectedRow();
        if (select!=-1) {
            arrayProd.remove(select);
            controlTab.eliminar(jTable1, select);
        }
        total();
        
    }//GEN-LAST:event_jButtonQuitarActionPerformed

    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        // registra las compras de productos realizadas
        
        controlTab.modificar(jTable1, arrayProd);
        
        if(tModel.getRowCount()!=0){
            if(controlProd.modificarTodo(arrayProd)){
                if(JOptionPane.showOptionDialog(this, " Registro Exitoso!!\n¿Desea limpiar las entradas?", "Mensaje",
                    JOptionPane.INFORMATION_MESSAGE, JOptionPane.INFORMATION_MESSAGE, null, new Object[]{" SI "," NO "},"NO")==0){
                    setVisible(false);
                    new RegistroCompra().setVisible(true);
                }
            }else
                JOptionPane.showMessageDialog(this, "Error en el registro de los productos","Mensaje de alerta",
                    JOptionPane.ERROR_MESSAGE, null);
        }
        else
            JOptionPane.showMessageDialog(this, "Debe Ingresar algun Producto","Mensaje de alerta",
                JOptionPane.ERROR_MESSAGE, null);
        
        
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

