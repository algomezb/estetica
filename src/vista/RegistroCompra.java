    public void total(){
        int n=tModel.getRowCount();
        double total=0;
        String temp;
        for (int i = 0; i < n; i++) {
            temp=(String)tModel.getValueAt(i,3);
            total+=Double.parseDouble(temp);
        }
        jTexTotal.setText(String.valueOf(total));
    }

