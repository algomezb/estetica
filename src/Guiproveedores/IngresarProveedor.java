    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        proveedor prov = new proveedor(jTextField8.getText(), jTextField9.getText(), activo.getText(), Integer.parseInt(jTextField1.getText()), Integer.parseInt(jTextField3.getText()), jTextField2.getText(), jTextField10.getText(), Integer.parseInt(jTextField4.getText()), jTextField5.getText(), jTextField6.getText(), jTextField7.getText());
        boolean validardatosvacios = validardatosvacios();
       
       
        if (validardatosvacios == false ) {
            manejoprov.insertar(prov);
            limpiar();
        } else{
             JOptionPane.showMessageDialog(this, "El registro ya se encuentra ingresado");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    public boolean validaringreso() {
        boolean resultado = false;
        int cedula = Integer.parseInt(jTextField1.getText());
        ResultSet rs = null;

        rs = (ResultSet) manejoprov.ValidarProveedor(cedula);
        try {
            if (rs.getRow() == 0) {
                resultado = false;

            } else {
                resultado = true;
                JOptionPane.showMessageDialog(this, "El registro ya se encuentra ingresado");

            }
        } catch (SQLException ex) {
            Logger.getLogger(IngresarProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        return resultado;
    }
