    public String fechaactual() {
        Date fecha = new Date();
        SimpleDateFormat formatofecha = new SimpleDateFormat("yyyy-MM-dd");

        return formatofecha.format(fecha);

    }

    public void valorarticulo() throws SQLException {
        int ids = Integer.parseInt(jTextField4.getText());
        ResultSet rs = null;

        rs = (ResultSet) manejopedi.consultarpreciopedido(ids);

        if (rs.next()) {

            jTextField5.setText(rs.getString(1));
            jTextField10.setText(rs.getString(2));
        }
    }

    public void calcularIva() {

        int valorunitario = Integer.parseInt(jTextField5.getText());
        int cantidad = Integer.parseInt(jTextField3.getText());
        double iva = 0.15;
        int totalsiniva = cantidad * valorunitario;
        double coniva = totalsiniva * iva + totalsiniva;
        int intconiva = (int) coniva;
        String cadena = String.valueOf(intconiva);

        jTextField7.setText(cadena);

    }

    public void codigopedido() throws SQLException {
        String valorpedido;
        ResultSet rs = null;
        String nuevocodigo;

        rs = (ResultSet) manejopedi.consultarpedido();
        if (rs.next()) {

            valorpedido = rs.getString(1);
            int pedidoint = Integer.parseInt(valorpedido);
            pedidoint = pedidoint + 1;
            nuevocodigo = String.valueOf(pedidoint);
            jTextField1.setText(nuevocodigo);

        }
    }
    public void NombreproveedorBD() throws SQLException {
        int ids = Integer.parseInt(jTextField9.getText());
        ResultSet rs = null;

        rs = (ResultSet) manejopedi.consultarnombreproveedor(ids);

        if (rs.next()) {

            jTextField11.setText(rs.getString(1));
           
        }
    }


